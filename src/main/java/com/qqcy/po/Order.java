package com.qqcy.po;

import com.qqcy.util.BaseBean;

public class Order extends BaseBean{
	private Integer o_id;

	private String o_createtime;

	private double o_sumprice;

	private String o_status;
	
	private User user;
	

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Integer getO_id() {
		return o_id;
	}

	public void setO_id(Integer o_id) {
		this.o_id = o_id;
	}

	public String getO_createtime() {
		return o_createtime;
	}

	public void setO_createtime(String o_createtime) {
		this.o_createtime = o_createtime;
	}


	public double getO_sumprice() {
		return o_sumprice;
	}

	public void setO_sumprice(double o_sumprice) {
		this.o_sumprice = o_sumprice;
	}

	public String getO_status() {
		return o_status;
	}

	public void setO_status(String o_status) {
		this.o_status = o_status;
	}
}
