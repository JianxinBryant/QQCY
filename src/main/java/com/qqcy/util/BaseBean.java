package com.qqcy.util;
/**
 * 所有实体的父类
 * @author Administrator
 *
 */
public class BaseBean {
	/**
	 * 启示的记录数
	 */
	private long start;
	/**
	 * 每页显示的记录条数
	 */
	private long length=10;
	
	public long getStart() {

		return start;
	}
	public void setStart(long start) {
		this.start = start;
	}
	public long getLength() {
		return length;
	}
	public void setLength(long length) {
		this.length = length;
	}
	
}
