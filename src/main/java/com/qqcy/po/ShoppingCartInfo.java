package com.qqcy.po;

import com.qqcy.util.BaseBean;
/**
 * 购物车相关信息实体
 * @author Administrator
 *
 */
public class ShoppingCartInfo extends BaseBean{
	private Integer shi_id;
	
	private double shi_vegenum;
	
	private String shi_status;

	private Vegetable vegetable;
	
	private User user;
	
	public String getShi_status() {
		return shi_status;
	}

	public void setShi_status(String shi_status) {
		this.shi_status = shi_status;
	}

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Vegetable getVegetable() {
		return vegetable;
	}

	public void setVegetable(Vegetable vegetable) {
		this.vegetable = vegetable;
	}

	public Integer getShi_id() {
		return shi_id;
	}

	public void setShi_id(Integer shi_id) {
		this.shi_id = shi_id;
	}

	public double getShi_vegenum() {
		return shi_vegenum;
	}

	public void setShi_vegenum(double shi_vegenum) {
		this.shi_vegenum = shi_vegenum;
	}
}
